# ------------------------- [OPERADORES DE COMPARACIÃ“N ] -------------------------
'''
Son los operadores que permiten comparar valores y devolver un valor booleano (True o False)

- Igual (==)
- Diferente (!=)  
- Mayor que (>)  
- Menor que (<)  
- Mayor o igual que (>=) 
- Menor o igual que (<=)  
'''

x = 10
y = 3

# Igual: Comparamos dos valores para determinar si son iguales
print("Igual:", x == y)  # False

# Diferente: Comparamos dos valores para determinar si son diferentes
print("Diferente:", x != y)  # True

# Mayor que: Comparamos si el primer valor es MAYOR al segundo valor
print("Mayor que:", x > y)  # True

# Menos que: Comparamos si el primer valor es MENOR al segundo valor
print("Menor que:", x < y)  # False

# Mayor o igual que: Comparamos si el primer valor es MAYOR O IGUAL al segundo valor
print("Mayor o igual que:", x >= y)  # True

# Menos o igual que: Comparamos si el primer valor es MENOR O IGUAL al segundo valor
print("Menor o igual que:", x <= y)  # False
